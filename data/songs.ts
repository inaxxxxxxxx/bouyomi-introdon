export function getRandomSong(excludeId?: number): Song {
  const available = excludeId
    ? songs.filter((s) => s.id !== excludeId)
    : songs
  return available[Math.floor(Math.random() * available.length)]
}

export function checkAnswer(song: Song, answer: string): boolean {
  const normalize = (s: string) =>
    s
      .toLowerCase()
      .replace(/[　 ]/g, "")
      .replace(/[ぁ-ん]/g, (c) =>
        String.fromCharCode(c.charCodeAt(0) + 0x60)
      )
  const normalTitle = normalize(song.title)
  const normalAnswer = normalize(answer)
  return (
    normalTitle === normalAnswer ||
    normalTitle.includes(normalAnswer) ||
    normalAnswer.includes(normalTitle)
  )
}
